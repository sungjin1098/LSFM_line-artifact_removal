class Discriminator(nn.Module):
    # Multi-scale discriminator architecture
    def __init__(self, input_dim, n_layer, dim, activ, pad, num_scales):
        super(Discriminator, self).__init__()
        self.n_layer = n_layer  # the number of conv layers
        self.dim = dim  # if dim=16, then # of channels become 1 -> 16 -> 32 -> ...
        self.activ = activ  # {relu, lrelu, prelu, selu, tanh, none} are possible
        self.pad_type = pad  # {reflect, replicate, zero} are possible
        self.input_dim = input_dim
        self.num_scales = num_scales
        self.downsample = nn.AvgPool2d(3, stride=2, padding=[1, 1], count_include_pad=False)
        self.cnns = nn.ModuleList()
        for _ in range(self.num_scales):
            self.cnns.append(self._make_net())

    def _make_net(self):
        dim = self.dim
        cnn_x = []
        cnn_x += [Conv2dBlock(self.input_dim, dim, kernel_size=4, stride=2, padding=1, activation=self.activ,
                              pad_type=self.pad_type)]
        for i in range(self.n_layer - 1):
            cnn_x += [Conv2dBlock(dim, dim * 2, kernel_size=4, stride=2, padding=1, activation=self.activ,
                                  pad_type=self.pad_type)]
            dim *= 2
        cnn_x += [nn.Conv2d(dim, 1, 1, 1, 0)]
        cnn_x = nn.Sequential(*cnn_x)
        return cnn_x

    def forward(self, x):
        for model in self.cnns:
            x = model(x)
            x = self.downsample(x)
        return F.sigmoid(x)


